    private void t_siswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_siswaMouseClicked
        int baris = t_siswa.getSelectedRow();
        if(baris != -1)
        {
            jtNIS.setText(t_siswa.getValueAt(baris, 0).toString());
            jtNama.setText(t_siswa.getValueAt(baris, 1).toString());
            jtTempatLahir.setText(t_siswa.getValueAt(baris, 2).toString());

            SimpleDateFormat date = new SimpleDateFormat("yyy-MM-dd");
            Date dateFormat = null;
            try
            {
                dateFormat = date.parse(t_siswa.getValueAt(baris, 3).toString());
            }
            catch(ParseException ex)
            {
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null,ex);
            }
            
            jDateChooser1.setDate(dateFormat);
           
            if("Laki - Laki".equals(t_siswa.getValueAt(baris, 4).toString()))
            {
                rbLaki.setSelected(true);
            }
            else
            {
                rbPerempuan.setSelected(true);
            }
            
            jtKelas.setText(t_siswa.getValueAt(baris, 5).toString());
            jtEmail.setText(t_siswa.getValueAt(baris, 6).toString());
            taAlamat.setText(t_siswa.getValueAt(baris, 7).toString());

        }
    }//GEN-LAST:event_t_siswaMouseClicked

