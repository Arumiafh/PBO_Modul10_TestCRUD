    private void buttonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveActionPerformed

        SimpleDateFormat dateFormat = new SimpleDateFormat("yyy-MM-dd");
        String tanggal = dateFormat.format(jDateChooser1.getDate());

        String JK = "";
        if(rbLaki.isSelected())
                {
                    JK = "L";
                }
                else
                {
                    JK = "P";
                }
        
        
        if(jtNIS.getText().equals("") || jtNama.getText().equals("")
                || jtTempatLahir.getText().equals("") || tanggal.equals("")
                || JK.equals("") || jtKelas.getText().equals("") 
                || jtEmail.getText().equals("") || taAlamat.getText().equals("")    
                        
                )
        {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data Anda","Error",JOptionPane.WARNING_MESSAGE);
        }
        else
        {               
           
            String SQL = "INSERT INTO t_siswa (NIS,Nama,TempatLahir,TanggalLahir,JK,Kelas,Email,Alamat)"
                    + "VALUES('"+jtNIS.getText()+"','"+jtNama.getText()+"','"+jtTempatLahir.getText()+"','"
                    +tanggal+"','"+JK+"','"+jtKelas.getText()+"','"+jtEmail.getText()+"','"+taAlamat.getText()+"')";
            
            int status = KoneksiDB.execute(SQL);
            
            if (status == 1)
            {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan","Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan","Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_buttonSaveActionPerformed

    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
        int baris = t_siswa.getSelectedRow();
        if(baris != -1)
        {
            String NIS = t_siswa.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM t_siswa WHERE NIS='"+NIS+"'";
            int status = KoneksiDB.execute(SQL);
            
            if (status == 1)
            {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus","Sukses", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus","Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih Dahulu","Error",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_buttonDeleteActionPerformed

