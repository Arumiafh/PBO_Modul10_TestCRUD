    private void selectData(){
        String kolom[] = {"NIS","Nama","TempatLahir","TanggalLahir","JK","Kelas","Email","Alamat"};
        DefaultTableModel dtm = new DefaultTableModel(null,kolom);
        String SQL = "SELECT * FROM t_siswa";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        
        try{
            while(rs.next())
            {
               String NIS = rs.getString(1);
               String Nama = rs.getString(2);
               String TempatLahir = rs.getString(3);
               String TanggalLahir = rs.getString(4);
               
               String JK = "";
               
               if("L".equals(rs.getString(5)))
               {
                   JK = "Laki - Laki";
               }
               else
               {
                   JK = "Perempuan";
               }
               
               String Kelas = rs.getString(6);
               String Email = rs.getString(7);
               String Alamat = rs.getString(8);
               String data[] = {NIS,Nama,TempatLahir,TanggalLahir,JK,Kelas,Email,Alamat};
               dtm.addRow(data);
            }
        }
        catch(SQLException ex)
        {
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        t_siswa.setModel(dtm);
    }
    
